fn decoy_quality_bps(
    entropy_bps: u64,
    freshness_bps: u64,
    distribution_bps: u64,
    lane_weight_bps: u64,
) -> u64 {
    (entropy_bps
        .saturating_mul(35)
        .saturating_add(freshness_bps.saturating_mul(25))
        .saturating_add(distribution_bps.saturating_mul(25))
        .saturating_add(lane_weight_bps.saturating_mul(15))
        / 100)
        .min(MAX_BPS)
}
fn low_fee_score(user_fee_bps: u64, target_fee_bps: u64, quality_bps: u64) -> u64 {
    let fee_component = if user_fee_bps <= target_fee_bps {
        MAX_BPS
    } else {
        MAX_BPS.saturating_sub(
            user_fee_bps
                .saturating_sub(target_fee_bps)
                .saturating_mul(800),
        )
    };
    (fee_component
        .saturating_mul(55)
        .saturating_add(quality_bps.saturating_mul(45))
        / 100)
        .min(MAX_BPS)
}
fn privacy_units(ring_size: u16, credit_units: u64, quality_bps: u64) -> u64 {
    let ring_component =
        u64::from(ring_size).saturating_mul(credit_units.max(1).ilog10() as u64 + 1);
    let quality_discount = MAX_BPS.saturating_sub(quality_bps / 3).max(1);
    ring_component.saturating_mul(quality_discount) / MAX_BPS
}
fn average<I>(values: I) -> u64
where
    I: Iterator<Item = u64>,
{
    let mut sum = 0_u64;
    let mut count = 0_u64;
    for value in values {
        sum = sum.saturating_add(value);
        count = count.saturating_add(1);
    }
    if count == 0 {
        0
    } else {
        sum / count
    }
}
