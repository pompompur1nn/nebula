    pub fn finalize_order(
        &mut self,
        order_id: &str,
        settlement_root: String,
        sponsor_netting_root: String,
    ) -> Result<()> {
        let order = self
            .fee_credit_orders
            .get_mut(order_id)
            .ok_or_else(|| format!("unknown order {order_id}"))?;
        ensure!(
            matches!(
                order.status,
                OrderStatus::SponsorReserved | OrderStatus::ScanRebated | OrderStatus::PoolMatched
            ),
            "order {order_id} is not finalizable"
        );
        if let Some(receipt_id) = order.sponsor_receipt_id.clone() {
            if let Some(receipt) = self.sponsor_receipts.get_mut(&receipt_id) {
                receipt.status = SponsorReceiptStatus::Finalized;
                receipt.netting_root = Some(sponsor_netting_root);
            }
        }
        if let Some(budget_id) = order.privacy_budget_id.clone() {
            if let Some(budget) = self.privacy_budgets.get_mut(&budget_id) {
                budget.status = BudgetStatus::Applied;
                budget.spent_units = budget.reserved_units;
                budget.remaining_units = 0;
            }
        }
        if let Some(rebate_id) = order.scan_rebate_id.clone() {
            if let Some(rebate) = self.scan_rebates.get_mut(&rebate_id) {
                rebate.status = RebateStatus::Paid;
                rebate.paid_height = Some(self.current_height);
            }
        }
        order.status = OrderStatus::Settled;
        order.settlement_root = Some(settlement_root);
        self.counters.settled_orders = self.counters.settled_orders.saturating_add(1);
        self.refresh_roots();
        Ok(())
    }
